    inner JOIN hhrrcargo as cargo on cargo.id = c.Cargo
    INNER JOIN hhrrunidad as unidad on unidad.id = c.Unidad
    INNER JOIN hhrrsucursal as sucursal on sucursal.id = c.Sucursal