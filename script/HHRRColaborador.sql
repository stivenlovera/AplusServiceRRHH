    c.FechaIngreso,
    c.FechaRatificacion,
    c.FechaFinalizacion,
    c.Aplica2aguinaldo,
    c.AguinaldoMes1,
    sucursal.NombreSucursal,
    c.ModoContrato,
    c.TipoContrato
